void HeatedGripControl_Init(void) {
    SET_HEATED_GRIPS_EN(LOW);
    SET_HEATED_SEAT_EN(LOW);
}

void HeatedGripControl_Run_100ms(void) {

    if (GET_SPARE_SWITCH_2_IN()) {
        SET_HEATED_GRIPS_EN(HIGH);
        SET_HEATED_SEAT_EN(HIGH);
    } else {
        SET_HEATED_GRIPS_EN(LOW);
        SET_HEATED_SEAT_EN(LOW);
    }
    
    //CAN_mcu_status_horn_set(GET_HORN_EN()); change this to heated grip status
}

void HeatedGripControl_Halt(void) {
    SET_HEATED_GRIPS_EN(LOW);
    SET_HEATED_SEAT_EN(LOW);
}

