void HornControl_Init(void) {
    SET_HORN_EN(LOW);
}

void HornControl_Run_100ms(void) {

    static uint8_t counter = 0;
    if (GET_HORN_SWITCH_IN()) {
        if (counter < HORN_MAX_TIME) {
            SET_HORN_EN(HIGH);
            counter++;
        } else {
            SET_HORN_EN(LOW);
        }
    } else {
        SET_HORN_EN(LOW);
        counter = 0;
    }
    
    CAN_mcu_status_horn_set(GET_HORN_EN());
}

void HornControl_Halt(void) {
    SET_HORN_EN(LOW);
}

